          <div>
             <h4 class="h4"> <i class="fas fa-gavel"> Case Law</i></h4>
            <p>
              In Nedcor Bank Ltd v Rundle101, the court was asked to decide when prescription recommences after interruption caused by the filing
              of claim against a company in liquidation. The court held that the running of prescription of a money debt, interrupted by the filing 
              of a claim against a company in liquidation, recommences on confirmation by the Master of the final liquidation and distribution account.
            </p>
          </div>
        <!-- /Case law -->
        <p>
          The relevant rules of our courts provide that once a summons is served, the plaintiff has twelve months to take further steps, after which the 
          summons will lapse. On the occurrence of the specified event, prescription must commence de novo, that is, it must start all over again. For example, 
          a creditor will normally have three years from the date that credit instalment sale agreement was entered into or the date of the most recent payment 
          (whichever is the later) to issue a summons to launch court action against a customer for payment, failing which the debt will prescribe. If the debtor 
          makes a single payment during this time or signs an acknowledgement debt, the three-year period will begin to run again from the new date.
        </p>

        <!-- Suspension -->
          <ul>
            <li>
              <strong>Suspension:</strong> certain impediments suspend the running of prescription until a period of one year has passed from the date when the date 
              when impediment ceased to exist. The following are examples of impediments that will serve to suspend prescription from running:
            </li>
            <ul>
              <li>The creditor is a minor</li>
              <li>The creditor is an insane person or a person under curatorship</li>
              <li>The creditor is prevented from interrupting prescription by vis major</li>
              <li>The debtor is outside the boarders of South Africa</li>
              <li>The debtor and creditor are married to each other</li>
              <li>The creditor and the debtor are partners and the debt is one arising from the partnership</li>
              <li>The creditor is a juristic person and the debtor is a member of the governing board of that juristic person</li>
              <li>The debt is the subject of dispute submitted for arbitration</li>
              <li>The debt is the subject of a claim filed against the estate of a deceased debtor or the insolvent estate of a debtor.</li>

            </ul>

            <li>
                <strong>Acquisitive prescription:</strong> the right of possession becomes the right of ownership if the thing is possessed with the intention of it being kept as if it were owned, for an uninterrupted period of thirty years.
            </li>
          </ul>
