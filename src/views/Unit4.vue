          <h5 class="h5" id="positionInEswatini">Position in Eswatini</h5>
          <p>
            Unlike in the Republic of South Africa where there is law regulating prescription, we do not have such a statute in Eswatini. This submission was clearly unpacked 
            in the local case of Shell South African Energy (Pty) Ltd v Fannie Constantine Mthethwa <sup>102</sup> The plea of prescription raised by the Plaintiff is based upon 
            the cause of action raised by the Defendant in his counterclaim, being the breach of the alleged contract of employment on 11 June 2008. The Plaintiff pleads that on 
            the Defendant’s case the Defendant’s cause of action arose on the date of the alleged breach (the termination of the alleged contract of employment) which was 11 June 
            2008. It is pleaded that the Defendant served and filed his counterclaim on 22 August 2011, which is more than three years after the debt and cause of action upon which 
            the Plaintiff relies arose. On this basis the Plaintiff avers that the counterclaim has been extinguished by prescription.
          </p>
          <p>
            It was held that that prescription in Roman Dutch Law has its source in the Roman Law. In South Africa the first statutory regulation was in the Cape where the Prescription
            Act of 1861 was passed. In the Transvaal, the Prescription Act 26 of 1904 was passed. This provided for a period of prescription in respect of proceedings at common law known 
            as actio redhibitoria or the actio quanti minoris of one year. It also provided for various prescriptive periods for other causes of action listed in sections 4 and 5 thereon. 
            In respect of matters for which a period was not fixed, the period was thirty years. (See South African Law Reform Commission discussion paper 12, July 2011).
          </p>
          <p>
            It was further stated in this case that in Swaziland the Roman Dutch Law and laws in force in the Transvaal as at 15th October, 1904 were received in terms of the reception statute 
            in 1907 In terms of the General Law and Administration No. 4 of 1907 section 3 – “the roman Dutch common law … shall be the law in Swaziland … the statutes in force in force in the 
            Transvaal on the 15th October, 1904, and the statutory regulations there under shall mutatis mutandis and as far as they may be applicable be in force in Swaziland”.
          </p>

          <p>
            The application of prescription was also tested in the local case of FirstRand Bank Limited v Nedbank Eswatini Limited <strong>103</strong>.
