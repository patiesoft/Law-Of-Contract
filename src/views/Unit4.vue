      </ol>

  <ol sytle="a">
        <h5 class="h5">
          <li>Performance or payment</li>
        </h5>
          
        <p>
          Thi4 is the most common way of ending contracts: each party 
          simply does what he or she was meant to do. If the debt is a money debt, the creditor is entitled 
          to payment in legal tender, that is, bank notes and coins. Bank notes must be accepted for any amount; 
          silver coins up to R4,00; and bronze or copper coins up to 10cents. Payment by cheque (other than post-dated cheques) 
          is equivalent to payment by cash, subject to the condition that the cheque will be met<sup>76</sup>.
        </p>
        <p>
          Where several debts are owed to the creditor, the creditor may inform the debtor that he or she will appropriate a payment 
          in respect of a certain debt, generally one that bears interest or the oldest debt. Interest will normally be paid before capital. 
          Where money is paid to a creditor in the mistaken belief that the debtor is indebted to the creditor, there is no contract to sue upon. 
          Instead, the debtor may rely on the doctrine of justified enrichment and recover his or her money by using the <i>condition debiti</i> 
          (a form of enrichment action founded in Roman law).
        </p>

        <h5 class="h5">
          <li>Release </li>
        </h5> 
        <p>
          This 4s known as waiver, or discharge by agreement. The parties may waive their rights and release each other from their respective 
          rights and obligations. In effect, this means that one or both parties abandon rights to the contract<sup>77</sup>.
        </p>

        <h5 class="h5">
          <li>Novation</li>
        </h5> 
        <p>
          It 4s possible to substitute a new contract for the original one (novation). The settlement of a dispute, or an agreement to compromise, 
          both discharge the original obligation, and a new one may be substituted in its place<sup>78</sup>.
        </p>
        <p>
          According to Kopel<sup>79</sup> , common forms of novation include:
          <ul>
            <li> <strong>Compromise (transaction):</strong> the parties by agreement, may settle an obligation for example the debtor tenders to the creditor a lesser
              amount than that which he or she owes, in full and final settlement.
            </li>
            <li> 
                  <strong>Cession (new creditor):</strong> previously discussed.        
            </li>
            <li><strong>Delegation (new debtor):</strong> previously discussed.</li>
          </ul>
        </p>

        <h5 class="h5">
          <li>Merger<i>(confusio)</i>  </li>
        </h5>

        <p>
          Mer4er occurs when the qualities of debtor and creditor coincide in the same person in respect of the same obligation, for example when a tenant 
          purchase the same property that he or she has been renting<sup>80</sup>.
        </p>

        <br>

        <!-- Case law -->
        <div>
        <h4 class="h4"> <i class="fas fa-gavel"> Case Law</i></h4>
          <p>
            In Grootchwaing Salt works Ltd v Van Tonder81, a tenant hired land with a salt pan. The lease gave the tenant rights to some of the adjoining ground, which was not let. The tenant later bought the ground over which the lease has existed. The court held that the rights over the adjoining ground had come to an end.
          </p>
        </div>
        <!-- /Case law -->
         <br>
       <h5 class="h5">
         <li>Impossibility of Performance</li>
         </h5>
        <p>
          A contract that is initially impossible to perform is void. Supervening impossibility of performance is voidable. The contract is discharged as  
          though it had been impossible from the very beginning<sup>82</sup>. All rights and duties between the parties fall away, and there is no breach or liability
          for damages. What has already been performed is usually recoverable on the basis of unjustified enrichment. Supervening impossibility may arise through an 
          act of God, where the law is changed to make performance dies or becomes incapacitated<sup>83</sup>.
        </p>
     
     <br>
        <!-- Case Law -->
        <div>
          <h4 class="h4"> <i class="fas fa-gavel"> Case Law</i></h4>
          <p>
            In <i>Peters Flamman and Company v Kokstad Municipality<sup>84</sup> </i>, a contract had been entered into a few years before the First World War to provide 
            lighting services for a period of twenty years. The partners of the suppliers were found to be enemy aliens, and interned. The business was
            wound up in terms of legislation that prohibited trading with enemies of the country. The court held that it had become impossible for the
            partners to render the performance required in terms of the contract.
          </p>
        </div>
        <!-- /Case Law -->
        <br>

        <p>
          Kopel<sup>85</sup> argues that supervening impossibility will not discharge a contract in any of the following cases:
          <ul>
            <li>The parties expressly or impliedly agreed that it should not <sup>86</sup></li>
            <li>The impossibility is relative or subjectively absolute.</li>
            <li>The impossibility is due to the fault of the person relying on it <sup>87</sup>.</li>
            <li>
              The debtor is in <i>mora</i> (has failed to perform on time); unless he or she can show that the event would have occurred in any case. For example, 
              if a building being sold is destroyed by an earthquake, the debtor will be able to claim supervening impossibility because the earthquake 
              would still have destroyed the building even if the debtor had performed on time.
            </li>
          </ul>
        </p>
   
   <br>
        <!-- Case Law -->
        <div>
          <h4 class="h4"> <i class="fas fa-gavel"> Case Law</i></h4>
          <p>
            In <strong><i>Hersman v Shapiro & Company<sup>88</sup></i></strong>, a dealer in corn sold a consignment in advance. He argued that because of a crop failure it was impossible to 
            deliver the goods. The court held that there had been no evidence that the right quality and quantity of corn was absolutely unobtainable in all 
            growing areas. The nature of speculative contract meant that it would have been contemplated by the seller that he might have to pay more than the 
            resale price if there was a scarcity of corn available. In <strong><i>Benjamin v Myers</i></strong> <sup>89</sup>, the tenant of a garage broke the law relating to the supply of petrol. 
            In terms of his lease, he had undertaken to provide petrol for sale at the garage. The court held that the tenant could not defend the action of landlord 
            in evicting him, as this was due to his wrongful conduct.
          </p>
        </div>
        <!-- /Case Law -->

        <h5 class="h5">
          <li> Set-off (Compensation)</li> 
        </h5>
        <p>
          If each party to a contract is indebted to the other party, the debts are balanced against each other. The lesser debt is discharged or both debts are discharged if they are equal90.
          In the case of <strong><i>Altech Data (Pty) Ltd v MB Technologies (Pty) Ltd)</i></strong><sup>91</sup>, for set off to operate, four (4) requirements must be met:
          <ul>
            <li>
              The debts must be due between the same parties in the same capacities.
            </li>
            <li>
              Both debts must be of money or movable property of the same kind.
            </li>
            <li>
              The debts must be liquid, that is, fixed or in amounts that are easy to determine. If a debt is liquid, this means that its exact amount is known and no evidence 
              is required to establish the amount owed.
            </li>
            <li>Both debts must unconditional, due, and payable.</li>
          </ul>
          The parties may agree to exclude any set-off operating<sup>92</sup>.
        </p>

        <h5 class="h5">
          <li>Prescription (Position in South Africa)</li>
        </h5 >
        <p>
          In terms of the South African Prescription Act <sup>93</sup>, it is possible to acquire rights, or be released from obligations, simply by the passing of time. In this way, prescription
          provides legal certainty<sup>94</sup>. The Institution of Legal Proceedings against certain Organs of State Act <sup>95</sup> relates to any cause of action where the state is liable for payment
          of damages. The Act applies t claims due before 28 November 2002 but not yet prescribed, and to claims due after 28 November 2002 <sub>96</sub> .
        </p>
        <p>
          An action against the state will include any action against any organ of state, including:
          <ul>
            <li>Any national government department</li>
            <li>Any provincial administration and department</li>
            <li>Any municipality at the local sphere of Government established under section 151 of the Constitution <sup>97</sup> </li>
            <li>Any functionary or institution exercising powers or performing functions under the Constitution98 or provincial constitution.</li>
            <li>The South African Marine Safety Authority.</li>
            <li>The South African National Roads Agency Limited.</li>
          </ul>
        </p>
        <p>
          Where the state is the debtor, notice must be given to the state within six months from the date on which the claim became due. Notice must be in writing, briefly setting out
          the facts giving rise to the claim, giving particulars that are within the knowledge of the claimant (including the known or estimated damages), and giving notice of intention 
          to institute the legal proceedings in question<sup>99</sup>.
        </p>
        <p>
          Where the state relies on the failure to give the proper notice in time, a claimant may apply to court for permission to provide the notice late. This is called condonation.
          Leave to institute legal proceedings may be granted if good cause exists for the failure to give proper notice, and the state is not unreasonably prejudiced by that failure. 
          Condonation cannot be granted after the debt has prescribed. Court process may not be served until 30 ordinary days have expired after the written notice was served on the State. 
          If court process is served permanently, it will not be invalid but will be regarding as having been served on the first day after expiry of the thirty-day period.
        </p>
        <p>
          There are two types of prescription: extinctive prescription and acquisitive prescription.
          <ul>
            <li>
            <strong> Extinctive prescription:</strong> this refers to the lapsing of the claim by the passing of time. In this way the law provides that creditors only should have a certain amount of time 
              in the time in which to claim debts owed to them, or they lose their rights. Extinctive prescription will intervene over different time periods in respect of different rights:
              <ul>
                <li>
                <strong>Thirty years:</strong> Debt secured by a mortgage bond judgement debt, or taxes, mineral royalties, or profits payable to the State.
                </li>
                <li>
                  <strong>Fifteen years:</strong> Debt owed to the state as result of loan, or sale or lease of land to the debtor by the State.
                </li>
                <li>
                  <strong>Six years:</strong> Debt arising from a bill of exchange, negotiable instrument or notarial contract.
                </li>
                <li>
                  <strong>Three year:</strong> any other debt or claim.
                </li>
              </ul>
            </li>
          </ul>
        </p>

        <p>
          Controversial cases involving prescription deal with situations where legal action is commenced a 
          long time after original events started. Courts are sometimes faced with difficult decisions as to when the cause of 
          action arose- if a plaintiff delays starting legal proceedings for too long, the defendant may argue successfully 
          that the claim has prescribed.
        </p>
          <br>
        <!-- Case law -->
          <div>
              <h4 class="h4"> <i class="fas fa-gavel"> Case Law</i></h4>
            <p>
              In  <strong><i>Van Zijl v Hoogenhout</i></strong>  <sup>100</sup>, a woman claimed she had been sexually abused by her uncle from when she was six until 
              she reached fifteen years of age. This lasted from 1958 to 1967. The abused caused long-term physical, psychological 
              and emotional damage. Although she reached majority in 1973, she instituted the action against the alleged abuser only
              in 1999. When she sued in 1999, the respondent alleged that the claim had prescribed in 1976, three years after she had 
              turned 21. The court held that prescription penalized unreasonable inaction, not inability to act. The evidence showed 
              that she had been aware until 1997, when she watched a television Programme dealing with child sexual abuse and later 
              talked to a friend who studies psychology, that it was not she, but her abuser, who was responsible for the assaults.
              It was only then that she acquired a rational appreciation that he was to blame and thus a meaningful knowledge of 
              the wrong that set prescription in motion.
            </p>
          </div>
        <!-- /Case law -->

        <!-- Interruption -->
        <ul>
          <li>
            <strong>Interruption:</strong>  on the occurrence of certain events, the time period for the prescription of rights may be extended for a 
            period of time: if the debtor expressly or tacitly acknowledges liability for the debt; in which case prescription starts 
            to run afresh from the date of that acknowledgement. A Debtor may acknowledge the debt, or debt or admit liability for it, 
            for example by making part payments. If the parties extend the due date for payment of the debt, for example a contractual 
            debt, in which case prescription starts to run from the new date. If the debt is claimed by serving court process on the debtor, 
            in which case a new period of thirty years starts to run from the date of final judgement. For example, the service of a summons 
            on the last day within the period for which extinctive prescription would normally apply has the effect of interrupting prescription 
            for a period of one year.
          </li>
        </ul>
        <!-- Interruption -->
        
