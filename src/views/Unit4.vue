    <h1 class="text-center chapter-topic  secondary-heading text-primary"><strong>Termination Of Contractual Obligation</strong></h1>

    <h4  class="text-left chapter-topic secondary-heading text-primary"><strong>Introduction</strong></h4>
    <p>
      A contract is terminated after being performed. Performance means the party must perform its own obligations
      as envisaged by the contract. Such performance should be made by the party upon whom the obligation is imposed and 
      that performance must be rendered to the person recognised by the law as being competent to receive the performance:
      See Hanornag SA (Pty) Ltd v Otto 1940 CPD 437.
    </p>
    <p>
      Before termination is said to have occurred in a contract, first and foremost the court must establish the parties’ 
      intention, taking into account the surrounding circumstances and everything which gives due to that mutual intention. 
      What the court does there is to find what the parties would have wished to have their minds been directed, whether the
      performance of that contract was to be specific and equivalent. This basic rule goes back to Roman law and simple stated:
      the nature of performance which discharges and obligation is that which is due under the contract.
    </p>
    <p>
      In the case of Bold v Cooper and Another 1949(1) SA 1195 (W) at 1200 Muray J said:
    </p>
    <div>
      <p>
        “The mere delivery of cheque, in a cash transaction, is in disputably not payment of cash-it is only when the cheque is paid 
        that the purchaser has complied with his obligation to pay cash and only then does dominium [of a movable thing sold] pass.”
      </p>
    </div>
    <p>
      From the foregoing example it is demonstrated that the obligation of the contracting parties is subject to the nature of the 
      performance which then discharges the obligations due under the contract. It is only when in the example of Cooper, that when 
      the cheque was paid the obligation was complied with and therefore one could have said the contract could then be terminated 
      after such performance.
    </p>
    <p>
      Secondly, should there be no clause as to the parties’ mutual intention the presumption that the performance was to be specific, 
      though this presumption is rebuttable in law. Thirdly, the court will in case of doubt be more likely to be in favour of equivalent 
      performance. If the manner of the act to be performed is immaterial or where performance, informa specific performance through no fault
      of the promissor: See the case of Peters Hammon vs Koksrud
    </p>
    <p>
      Fourthly, the act of performance tendered where such is permissible must in the just instance be an equivalent to that mentioned in the contract or be of such a nature that it can make no material difference to the promise. What we must note finally when dealing with the termination of a contract and the considerations thereto, the court`s paramount obligation is to do justice between man and man. In doing this, 
            the court is guided by the terms and circumstances in the contract under consideration.
    </p>
