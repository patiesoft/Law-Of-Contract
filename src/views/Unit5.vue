              <h1 id="formsOfBreach" class="text-center chapter-topic  secondary-heading text-primary"><strong>Forms Of Breach</strong></h1>
              <ol type="i">
                <p>
                  In modern law, the generic concept of breach has been fractured into a number of distinct types or forms of breach, creating the 
                  impression that the conduct in question must first be slotted into one of these specific doctrinal pigeonholes before it can 
                  attract consequences of breach of contract <sup><a id="117" href="#117r">117</a></sup>
                  . Before we discuss the various types of breach, it is imperative that we understand the meaning of 
                  the term 
                  <em>“mora”</em> 
                  in the context of contracts. GA Mulligan <sup><a id="118" href="#118r">118</a></sup>
                  defined mora as follows:
                </p>
                <div >
                </div>
                <div >
                  <p>
                    <em>
                      <blockquote class="blockquote text-left">
                        “In the law of contract mora means delay, without lawful excuse, of performance of a contractual duty; in other words mora 
                        is wrongful failure to perform timeously”. 
                      </blockquote>
                    </em> 
                  </p>
                </div>
                <p>
                  The specific forms of breach recognized by our law are as follows:
                </p>
                <em><strong><li>Mora Debitoris</li></strong></em>
                <p>
                  According to Hutchison<sup><a id="119" href="#119r">119</a></sup>
                  , <em>mora debitoris</em> is the failure of debtor, without lawful excuse, to make timeous performance of a positive 
                  obligation that is due and enforceable and still capable of performance in spite of such failure. It is evident that, for its fulfilment, 
                  any obligation must have a time limit, otherwise the obligation will be valueless. If the debtor neglects or fails to perform timeously 
                  in other words, when performance is due, where performance remains possible in spite of such failure he is guilty of breach of contract 
                  specifically called <em>mora debitoris</em>. The defaulting party is therefore said to be in mora. <em>Mora debitoris</em> refers to the time of performance 
                  alone and not to the nature of performance. For <em>mora debitoris</em> to arise, the debt in question must be due and enforceable. In particular 
                  the time for the performance must have arrived, the obligation must not be subject to an unfulfilled suspensive condition, the debt must 
                  not have become prescribed and it must not be necessary that the creditor still has to do something prior to the debtor’s 
                  obligation to perform. 
                </p> 

                <!--Expected Outcomes -->
                <ol type="a">
                  <p>
                    <em>Mora debitoris</em> consists in an omission, a failure to perform on time, it can only occur in respect of a positive obligation 
                    (obligatio faciendi). 
                  </p> 
                  <strong><li>Requirements for <em>mora debitoris</em></li></strong>
                  <p>
                    The following requirements must be met before the debtor can be said to be in <em>mora:</em>
                    <ul>
                      <li>The debt must be due and enforceable</li>
                      <li>
                        The time for performance must have been fixed, either in the contract or by subsequent demand for performance, and the debtor must have 
                        failed to perform timeously
                      </li>
                      <li>Such failure to perform on time must be without lawful excuse.</li>
                    </ul>
                  </p>
                  <p>
                    <em>Mora debitoris</em> gives rise to the usual remedies for breach, to be covered in the next unit. That is damages and in limited circumstances the 
                    right to rescind the contract                      
                  </p>

                  <p>
                    From the above, it is clear that <em>mora debitoris</em> occurs when there is a delay in performance only if the date of performance is fixed and that day has 
                    arrived. A time for performance can be set in two ways. Firstly, it may be agreed upon in the contract either expressly or tacitly. In this case the debtor 
                    falls into mora automatically when the specified date arrives. This is also known as <strong>mora ex re.</strong>  
                  </p>
                  <p>
                    Secondly, when no time for performance is laid down in the contract, the creditor must set a time by making demand on the debtor to perform at the set time. 
                    When this time arrives and the debtor fails to perform, he falls in <strong>mora ex persona.</strong> 
                  </p>
                  <strong><li>Remedies of Creditor in the Case of Mora Debitoris</li></strong>
                  <p>
                    Remedies are discussed in detail in unit 6 of this module. The discussion follows is to explain the remedies as applicable to the particular form of breach 
                    which in this study is <em>mora debitoris.</em>
                  </p>
                  <p>
                    There are three possible remedies open to a creditor whose debtor is in <em>mora</em>, namely:
                    <ul>
                      <li>Performance by the debtor may be enforced.</li>
                      <li>Creditor may cancel the contract in certain circumstances.</li>
                      <li>Creditor may claim damages with regard to any loss suffered.</li>
                      <li>Damages may be claimed together with specific performance or with cancellation.</li>
                    </ul>
                  </p>
                  <strong><li>The Circumstances under which the Creditor May Resile from the Contract</li></strong>
                  <p>
                    Rescission is discussed in detail in Unit 6 of this module. This discussion is solely for explaining rescission as a particular remedy applicable in mora 
                    debitoris. As in the case of our common law, modern law starts from the premise that <em>mora debitoris</em> does not in itself entitle the creditor to resile from 
                    a contract. 
                  </p>
                  <p>
                    Cancellation of a contract of the ground of mora debitoris is allowed when the time is of the essence of the contract, and time of the essence in any of the following 
                    circumstances: 
                    <ul>
                      <li>
                        When the contract contains a cancellation clause. See the case of <em><strong>Van der Merwe v Brink</strong></em>
                        <sup><a id="120" href="#120r">120</a></sup>
                    
                      </li>
                      <li>
                        Where there is a tacit cancellation clause. This is as per Herbstein J in Goldtein & <em><strong>Wolff v Maison Blanc (Pty) Ltd</strong></em> at 
                        <i>452-453</i>                      
                      </li>
                      <li>Where the creditor has given the debtor a notice of rescission. See the case of <em><strong>Goldberg v Buystendag Boerdery Beleggings (Edms) Bpk</strong></em>
                       <sup><a id="121" href="#121r">121</a></sup>
                        
                      </li>
                    </ul>
                  </p>
                  <br>
                  <div class="example z-depth-2 bg2 p-4">
                    <h6 class="text-left">Example</h6>
                    <ul>
                      <li>
                        <em>
                          Where a purchaser is obliged to accept delivery of the things at the seller’s shop or where he is obliged to give instructions in regard to time and place of delivery and the purchaser fails to do so. 
                        </em>
                      </li>
                      <li>
                        <em>
                          An artist who agrees to paint a person’s portrait can only do so if the person to be in the portrait sits for him. 
                        </em>
                      </li>
                      <li>
                        <em>
                          A landlord must accept the rent from his or her tenant. 
                        </em>
                      </li>
                    </ul>
                  </div> 
                </ol>
                <!--Expected Outcomes -->

                <br>
                <br>

                <em><strong><li>Mora Creditoris</li></strong></em>
                <p>
                  It is not only a debtor who can commit a breach but a creditor can also act wrongfully. Where the cooperation of the creditor is 
                  necessary for the fulfilment of the obligation of the debtor, the creditor is guilty of breach of contract if he fails to co-operate 
                  timeously and performance remains possible
                  <sup><a id="122" href="#122r">122</a></sup>                   
                  . We then speak of <em>mora creditoris</em>. The role of a creditor in relation to a contract obligation is a passive one, namely to 
                  receive rather than to make the performance that is due in terms of the obligation. Hutchison
                  <sup><a id="123" href="#123r">123</a></sup>
                   goes on to say that it might therefore seem strange that the creditor can commit a breach of contract in 
                  his or her capacity as creditor. in such cases, the creditor is obliged to lend his or her co-operation, and a culpable failure to so 
                  timeously constitutes the form of breach known as <em>mora creditoris</em>
                  <sup><a id="124" href="#124r">124</a></sup>
                  . 
                </p>
                <ol type="a">
                  <li>
                    <h6><strong>Requirements for Mora Creditoris</strong></h6>
                    <br>
                    <p>
                      The requirements for mora creditoris are in many respects similar to those for <em>mora debitoris</em>, they are as follows: 
                      <ul>
                        <li>
                          The debtor must be under an obligation to make the performance to the creditor, but the debt need not be enforceable since
                          mora creditoris can occur even in respect of a natural obligation
                          <sup><a id="125" href="#125r">125</a></sup>                           
                        </li>
                        <li>
                          The cooperation of the creditor must be necessary for the proper performance by the debtor of his or her obligation. Thus, 
                          there can be no mora creditoris in respect of a negative obligation, or in respect of a positive obligation that can be 
                          discharged without the cooperation of the creditor.
                        </li>
                        <li>
                          Usually, the debtor must tender performance to the creditor. What constitutes a proper tender of performance depends on 
                          the nature and content of the obligation in question. 
                        </li>
                        <li>
                          The creditor must delay in accepting performance (e.g. not being present at the stipulated time to receive performance)
                          <sup><a id="126" href="#126r">126</a></sup>
                           
                        </li>
                        <li>
                          The delay in performance must be due to the fault of the creditor. If it is caused by vis <em>maior</em> or <em>fortuitus</em>, <em>mora creditors</em> is excluded.
                        </li>
                      </ul>
                    </p> 
                  </li>

                  <li>
                    <h6><strong>Remedies for Mora Creditoris</strong></h6>
                    <br>
                    <p> 
                      <ul>
                        <li>
                          <strong><h6>Cancellation</h6></strong>
                          <p>The debtor may cancel the contract in exactly the same circumstances as those in which the creditor may cancel for <em>mora debitoris.</em></p>  
                        </li>
                        
                        <li>
                          <strong><h6>Damages</h6></strong>
                          <p>
                            <em><strong>In the case of Ranch International Pipelines (Transvaal)(Pty) Ltd v LMG Construction</strong></em> (City) (Pty) Ltd supra at 882 it was stated that whether 
                            the debtor elects to cancel or affirm the contract, he or she is entitled to damages for any loss that has been suffered as a result of the 
                            <em>mora.</em> 
                          </p>
                        </li>
                        <li>
                          <strong><h6>Specific performance</h6></strong>
                          <p>
                            If the debtor elects to abide by the contract, he or she mat in suitable circumstances obtain an order of specific performance compelling the creditor to cooperate    
                            <sup><a id="127" href="#127r">127</a></sup>                            
                          </p>
                        </li>
                        <li>
                          <strong><h6>Counter-performance</h6></strong>
                          <p>
                            In the case of a reciprocal contract, the creditor’s delay in receiving the debtor’s performance clearly does not relieve him or her of the duty to make his or her 
                            own counter performance. Faced with the prospect of being compelled to perform his or her obligation, the creditor is likely to then accept the debtor’s performance 
                            <sup><a id="128" href="#128r">128</a></sup>
                           . 
                          </p>
                        </li>
                        <li>
                          <strong><h6>Care of article and Supervening Impossibility of Performance</h6></strong>
                          <p>
                            According to Hutchison
                            <sup><a id="129" href="#129r">129</a></sup>                            
                            , <em>mora creditoris</em> alleviates the duty of the debtor to take care of an article that he or she has to deliver. Usually the debtor is liable for 
                            any damages caused to the article by his or her negligence; once <em>mora creditoris</em> intervenes, the debtor’s responsibility is limited to <em>dolus</em> (intent) and <em>culpa lata</em> 
                            (gross negligence).
                          </p> 
                        </li>
                        <li>
                          <strong><h6>Effect on Security</h6></strong>
                          <p> 
                            Mora creditoris releases sureties, but is effect on the existence of a mortgage, pledge or lien is uncertain 
                            <sup><a id="130" href="#130r">130</a></sup>. 
                          </p>
                        </li>
                        <li>
                          <strong><h6>Discharge of Debt</h6></strong>
                          <p> 
                            Unless he or she validly cancels the contract or obtains an order compelling the creditor to cooperate, it is not clear how the debtor can discharge the debt without having to wait until the 
                            period of prescription has run or until performance has become impossible 
                            <sup><a id="131" href="#131r">120</a></sup>
                           . 
                          </p>
                        </li>
                      </ul>
                    </p> 
                  </li>
                </ol>
                <em><strong><li>Positive Malperformance</li></strong></em>
                <p>
                  <strong>Positive malperformance</strong> occurs when a debtor has performed in a way that is contrary to the terms of the contract. Positive malperformance occurs 
                  when a debtor has performed in a way that is contrary to the terms of the <strong>contract</strong>. There are two situations where this may occur:
                  <ul>
                    <li>The debtor has performed, but in an incomplete or inadequate manner.</li>
                    <li>The debtor has performed an act that he/she may not do according to the terms of the contract.</li>
                  </ul>
                </p>
                <em><strong><li>Repudiation</li></strong></em>
                <p>
                  Repudiation may be defined as ‘words or positive conduct indicating an unequivocal intention on the part of either of the parties not to be 
                  bound or not to be fully bound by the contract. Apart from the time element in breach, repudiation is where one party to a contract evinces 
                  (discloses) an intention to no longer be bound by the contract, repudiation can be expressed or by conduct. As an independent from of 
                  breach, repudiation was virtually unknown to the Roman-Dutch law, at any rate in its anticipatory form. According to Hitchison, the 
                  doctrine is derived from English law, a fact that explains not only the ready reference to English cases on the topic, but also many of 
                  the peculiar features of this novel form of breach 
                  <sup><a id="132" href="#132r">132</a></sup>
                  . 
                </p>
                <p>
                  In the local case of <strong><em>The New Mall (Pty) Ltd v Tricor International (Pty) Ltd</em></strong>
                  <sup><a id="133" href="#133r">133</a></sup>  
                   , the learned judge stated as follows: 
                </p> 
                <div>
                </div>
                <div>                  
                      <blockquote class="blockquote text-left">
                        “In every case the question of repudiation must depend on the character of the contract, the number and weight of the wrongful acts or 
                        assertion, the intention indicated by such acts or words, the deliberation or otherwise with which they are committed or uttered, and the 
                        general circumstances of the case.”
                      </blockquote>
                   
                </div>
                <p>
                  The learned Judge went on to scrutinize Clause 11 of the contract which stated that:
                </p>
                <p>
                  <em>“Should either party commit <u>a material breach of any provision</u> </em><br>
                  <u id="dotted_underline">.                                        .</u>[my emphasis] 
                </p>
                <p>
                  This phrase suggest to me that it was incumbent upon the applicant to show to the respondent that the remedy so demanded was as a result of 
                  a <em>“material breach”</em> and therefore the statements <em>“the foundation of the contract has been completely destroyed”</em> and <em>“irretrievable 
                  breakdown of trust and confidence”</em> was nothing else by information tautologous to the fact that the breach complained of was material. 
                </p>
                <p>
                  <em>In casu</em> the words so complained of are not indicative of refusal by applicant to act on a minor provision or any term of the contract.  
                  They are merely on expression of the extent at which the terms of the contract has been violated by respondent.  Respondent as already 
                  mentioned, must prove on a balance of probability an <em>“unequivocal intention”</em> [as per <strong>Rumpff J.A.</strong> in <strong>Van der Heerden</strong> <em>op. cit.</em>] on the part 
                  of the applicant not to be bound by the contract. 
                </p>
                <strong><li>Prevention of Performance</li></strong>
                <p>
                  As alluded to above in Unit 3 that it may happen that the party to whom performance is due (for example an owner for whom a house is being built) prevents the other party from completing 
                  the performance (for example the owner refuses the building contractor access to the site after the house is half built). We have also seen that initial impossibility of performance 
                  prevents a contract from arising at all, and have referred briefly to the fact that the supervening impossibility of performance generally discharges the contract. 
                </p>
                <p>
                  However, where performance on either side becomes impossible after the conclusion of the contract owing to the fault of either the debtor or the creditor, the contract is not terminated, 
                  but the party who rendered performance impossible is guilty of breach of contract known as prevention of performance
                  <sup><a id="134" href="#134r">134</a></sup>
                  .It is not necessary that the performance should be objectively impossible in order for the breach to arise; subjective impossibility will suffice 
                  <sup><a id="135" href="#135r">135</a></sup>. In other words, the breach is committed even when the impossibility attaches only to the particular debtor; for example A sells a cow to B and thereafter 
                  prevents himself from fulfilling the contract by selling and delivering the cow to C.  In such a case, performance is not altogether impossible, A might re-acquire the cow from C in order to 
                  deliver it to B- but this is highly unlikely to occur.   
                </p>
                <p>
                  In theory, there is a difference between subjective prevention of performance and repudiation- namely that the former can be committed negligently, whereas repudiation requires an unequivocal 
                  intention to repudiate.
                  <ol type="a">
                    <li>
                      <h6><em>Fault</em></h6>
                      <p>
                        According to <strong><i>Hutchison</i></strong>, fault is an important element of breach since in its absence supervening impossibility will terminate the contract. Although Roman and Roman-Dutch law required 
                        various degrees of fault in respect of contracts involving duty to deliver or to re-deliver an article in one’s custody, it seems that in the modern law there is only one standard of 
                        fault, namely, the care that could be expected from an ordinary, reasonable person in the circumstances . 
                      </p>

                    </li>

                    <li>
                      <h6><em>Remedies</em></h6>
                      <p>
                        The remedies available to the innocent party follow the usual pattern, except that specific performance is of course excluded. On the assumption that the inability to perform is total, the breach will 
                        always be sufficiently serious to merit rescission. Thus where the debtor is to blame for his or her inability to perform, the creditor may either:
                      </p>
                      <ul>
                        <li>
                          Cancel the contract, recover any performance already made on his or her side, and claim damages in respect of losses suffered due to non-fulfilment of the contract. 
                        </li>
                        <li>
                          Abide by the contract, perform his or her side of the agreement and claim damages in lieu of performance from the debtor. See the case of <strong>BK Tooling (EDMS) BPK v Scope 
                          Precision Engineering (EDMS) BPK</strong>  
                          <sup><a id="136" href="#136r">136</a></sup>
                        </li>
                      </ul>

                    </li>

                  </ol>

                </p>

                
              </ol>
