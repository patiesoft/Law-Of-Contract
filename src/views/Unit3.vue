            <h1 class="text-center chapter-topic secondary-heading text-primary"><strong>Reciprocity Of Obligations </strong></h1>
          <p>
            In the leading case of <strong><i>BK Tooling (Edms) Bpk v Scope Precision Engineering (Edms) Bpk<sup>59</sup></i></strong> , the Appellate Division stated that reciprocal obligations are obligations that have been created in exchange of each other. A contract that contains reciprocal obligations is also called a reciprocal contract. The distinctive factor of a reciprocal contract, therefore, is that it is essentially aimed at accomplishing an exchange of obligations. Most contracts that create obligations for both parties will be regarded as reciprocal. 
            In the local case of <strong><i>G.S Franco Investments (Pty) Ltd v Carr Corp Investments (Pty) Ltd<sup>60</sup></i></strong>  Masuku J, referring to R.H. Christie’s analysis of the B.K. TOOLING case <i>(supra) per Jansen J.A.<sup>61</sup></i> , state the following : -
          </p>
          <div class="example z-depth-2 bg2 p-3 m-4">
          <p> 
            <i>
          "In B.K. Tooling (Edms) Bpk vs Scope Precision Engineering (Edms) Bptk 1979, S.A. 391 (A), the Appellate Division reviewed in some detail the history and scope of what have become known as the principle of reciprocity and the exceptio non adimpleti contractus. The principle of reciprocity recognises the fact that in many contracts the common intention of the parties, expressed or unexpressed, is that there should be an exchange of performances, and the exceptio gives effect to the recognition of this fact by serving as a defence for the defendant who is sued on the contract by a plaintiff who has not yet performed or tendered to perform. "
          </i>
          Similarly, Corbett J.  in <strong><i> Ese Financial Services (Pty) Ltd v Cramer <sup>62</sup></i></strong> stated that-
          </p>
          <p>
            <i>
          "In a bilateral contract, certain obligations may be reciprocal in the sense that the performance of one may be conditional upon the performance or tender of performance, of the other. The reciprocity may itself be bilateral in the sense that the performance, or tender of performance, of them represent concurrent conditions, that is, each is conditional upon the other. A ready example of this would be delivery of <strong>res vendita</strong> and payment of the purchase price under a cash sale...Alternatively, the reciprocity may be one-sided in the complete performance of his contractual obligation by the other party may be a condition precedent to die performance of his reciprocal obligation to other party. In other words the obligations, though inter¬dependent, fall to be performed consecutively. An example of this would be a <strong>locatio-conductio operis</strong> whereunder the <strong>conductor operis</strong> is normally obliged to carry out the work which he is engaged to do before the contract money can be claimed. In such a case, the obligation to pay the money is conditional on the pre-performance of the obligation to carry out the work, but, of course, the converse does not apply....
            </i>
          </p>
          <p>
            <i>
          Reciprocity of obligations does not depend, however, merely upon the time fixed for performance thereof. Thus, the mere fact that the contract specifies that the obligations are due to be performed on the same day does not lead to the inference that the parties intended them to be reciprocal.... For reciprocity to exist there must be such a relationship between the obligation by the one party and that due by the other party as to indicate that one was undertaken in exchange for the performance of the other and in cases where the obligations are not consecutive, vice versa. "
            </i>
          </p>
          </div>
            <hr align="left" width="50%" color="black"> 
              <p>
