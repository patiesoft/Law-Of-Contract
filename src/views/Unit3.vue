            <h1 class="text-center chapter-topic secondary-heading text-primary"><strong>Specific Perfomance</strong></h1>
                  <p>
                      One critical aspect of contracts is that, in terms of the law, an aggrived party can seek an order of
                      specific performance from Court in order to compell the defaulting party to perform. The right to order
                      specific performance emanates from the fact that contrats create obligations which must be performed and 
                      each party is expected to deliver its obligations.You will recall that in Law203we discussed that a contract
                      creates obligations or a duty to perform.Therefore, once a valid contract is concluded, the parties must honour 
                      their obligations, and that is where the element of performance comes into operation.
                  </p>
                  <p>
                      Let us begin by examining what is specific performance in contract.In accordance with the general principle <i>pacta sunt servanda</i><sup>44</sup> 
                      ,the parties to a contract are obliged to fulfil their contractual undertakings<sup>45</sup> . It follows that 
                      each party has a right to expect and to claim perfomrance from the other. Specific performance therefore forms one 
                      of the primary remedies for breach of contract. An order of specifc performance is an order of court compelling a
                      contracting pary to render the very performance that the party agreed to make in terms of the contract<sup>46</sup> .
                  </p>
                  <p>
                      In the case of <strong><i>Jossa v Barclays Western Bank Ltd</i></strong><sup>47</sup> it was held that specific performance 
                      can be claimed as soon as the obligation to make performance is due and enforceable; it is not necessary that the debtor
                      should have fallen into <i>mora</i>. This tells us that specific perfomance is a right that vests on or accrues to a party
                      to a contract upon a valid conclusion of the contract. 
                    </p>
                    <p>
                      Courts do not issue the order for specific performance willy nilly, there are ceratin guidelines that need to be followed. We will now proceed to discuss the scope for specific performance as well as discretion exercised by the courts when dealing with specific performance. It is also worth mention that before the court issues any order, there are certain requirements that must be met, similarly with the order for specific performance. These requirements will also be covered in this section. 
                    </p>   
                  
                      <hr align="left" width="50%" color="black"> 
                      <p>
                        <sup>44</sup>This is a Latin term which connotes the principle that “contracts must be kept”. In its common sense, the principle refers private contracts, stressing that contained clauses are law between the parties and implies that nonfulfillment of respective obligations is a breach of the pact. 
                        <br>
                        <sup>45</sup>Dale Hutchison “ The Law of Contract in South Africa”, 3rd Ed, Oxford, p. 333
                        <br>
                        <sup>46</sup>Dale Hutchison “ The Law of Contract in South Africa”, 3rd Ed, Oxford, p. 333
                        <br>
                        <sup>47</sup>1990 (1) SA 575 (T)
                      </p>
         
 

            
