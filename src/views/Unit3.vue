              <div>
                    
                  <h4 class="h4">
                  Discretion of the Courts in Specific Performance
                  </h4>
                    <p>
                      Note that in Roman-Dutch law, the right to specific performance was almost absolute. Due to the influence of English law, however, our courts later came to accept that the remedy of specific performance is a discretionary one
                      which can be refused on the grounds of equity or public policy - see in this regard the case of<i> Heynes v Kingwilliamsontown Municipality</i><sup>49</sup> .
                      In practice, various guidelines were developed for the exercise of the court’s discretion, and over the years these guidelines have hardened to the point where they have almost attained the status of rules of law, effectively 
                      taking away the discretion of the court.   
                      A case illustrating this position is that of <strong><i>Schierhout v Minister of Justice</i></strong><sup>50</sup>.This gradual fettering of the court’s discretion attracted much criticism and was eventually rejected by the 
                      Appellate Division in the leading case of <strong><i>Benson v SA Mutual Life Assurance Society</i></strong><sup>51</sup>.In the <strong><i>Benson</i></strong> case, the court forcefully reasserted that, in principle, <i>“contracting 
                      parties have a right to specific performance, subject only to the court’s overriding discretion to refuse the order in appropriate cases...”</i>
                    </p>

                    <br>

                    <p>
                      In the local case of <strong><i> Nondlela Suzan Konyana v Ngwane Park Township (Pty) Limited <sup>52</sup></i></strong> ,  the most recent local judgment on this topic, Foxcroft JA, at paragraph [3] of his so far unreported judgment, stated that:
                    </p>

                    <div class="example z-depth-2 bg2 p-3 m-4">
                          <i>“Ms. Van der Walt referred us to the well-known decision in South Africa of<strong> Haynes v King Williamstown Municipality<sup>53</sup></strong>  She submitted, citing a passage from that 
                          judgment, that specific performance should not be granted in this matter, as it –“would operate unreasonably hardly on the defendant.” In the more recent 
                          decision of the same court in <strong>Benson v S.A Mutual Life Assurance Society<sup>54</sup></strong>  Hefer JA, in an “elucidation” of what De Villiers AJA had said in <strong>Haynes v King Williamstown Municipality</strong>,
                          said the following:
                          </i>
                          <i>“This does not mean that the discretion is in all respects completely unfettered.  It remains, after all, a judicial discretion and from its very nature arises the requirement that it is not to be exercised
                            capriciously, nor upon a wrong principle (Ex parte Neethling (supra at 335)).  It is aimed at preventing an injustice – for cases do arise where justice demands that a plaintiff be denied his right
                            to performance – and the basic principle thus is that the order which the Court makes should not produce an unjust result which will be the case, e.g.  if, in the particular circumstances, 
                            the order will operate unduly harshly on the defendant.” 
                          </i>
                        </div>

                        <p>
                          From the reading of this judgment it is clear that the Court will refuse to order specific performance where to do so would cause undue hardship to the defaulting or third parties. 
                          In this case, the respondent municipality had bound itself by contract to release 250 000 gallons of water per day to Haynes, but failed to honour this obligation during a period 
                          of severe and unprecedented drought. Haynes’ claim for specific performance was refused in the grounds that to have ordered it
                          <i>“would have very great hardship not 
                          only to the respondent but to the citizens of King Williamstown to whom the respondent owed a public duty to render an adequate supply of water”. 
                          </i>
                        </p>

                        <p>
                          All the above mentioned authorities were instrumental in the recent Supreme Court judgment of <strong><i>Tsabedze v University of Eswatini <sup>55</sup></i></strong> . Kerr<sup>56</sup> boldly asserts that there is no onus of proof
                          in regard to the exercise of the discretion. 
                        </p>
                        <div class="example z-depth-2 bg2 p-3 m-4">
                          <i>
                            “The discretion to refuse to order specific performance being the Court’s, the Court is entitled to arrive at
                              its decision without being bound by any rules relating to the onus of proof.” Kerr also states the principle that,
                            “a claim for specific performance is only competent if the plaintiff has performed or is ready and willing to perform any obligations
                              resting on him which are due and reciprocal.” 
                          </i>
                        </div>
                        
                        <p>
                          You are required to study the above judgments so as to gain thorough understanding of specific performance as a right and as a remedy, and most importantly, the exercise of discretion by the court in determining its enforceability. 
                        </p>
